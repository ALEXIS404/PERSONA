public class Persona {

    //Atributos
    private String nombre;
    private int edad;
    private String cedula;
    private char sexo;
    private double peso;
    private double altura;

    
    //Contructores
    public Persona(String _nombre, int _edad, String _cedula, char _sexo, double _peso, double _altura) {

        this.nombre=nombre;
        this.edad=edad;
        this.cedula=cedula;
        this.sexo=sexo;
        this.peso=peso;
        this.altura=altura;

    }
}


